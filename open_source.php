		<div class="clear"></div>
		<br /><p>You may recognize the logos above. The penguin (<em>Tux</em>) is the mascott of <em><strong>Linux</strong></em>, which is a free operating system, in the middle we've got <em><strong>Firefox</strong></em> (a browser), surely one of the most successful open source projects, and <em><strong>LibreOffice</strong></em> is a free office suite.</p>
		<br /><p>The term <em><strong>open source</strong></em> applies to software which is distributed for free and of which everyone is allowed to access the source code. Thus it's possible for others to take care of the software, extend it, individualize it for special tasks or use it (or parts of it) to create own software. In other words: allowing others to access you code makes it possible that they learn and benefit from it. If a lot of people allow everyone to access their resources, collective knowledge and development are possible.</p>
	</div>

	<div class="innercontent_l">
	<h1>Why use?</h1>
		<p>So ... why should one use open source software? First of all: it's free. Why spend any money when you don't have to? Furthermore, open source software is more likely to be customizable to your needs &mdash; commercial software usually is crippled software (the developer is more interested in his software to never malfunction than in you having the possibility to get the full potential out of it). Lastly: using closed systems, you never know what is done with your data. As for open source, you just can look "behind the scenes".</p>
	</div>

	<div class="innercontent_l">
		<h1>Why contribute?</h1>
		<p>If there is one concept in technology and culture that I support unrestrictedly and enthusiastically, it is <strong>open source</strong>. I find the thought of limiting or preventing the access to information, knowledge and cultural goods highly disturbing. Of course, it is a way to make money in our thoroughly commercialised world and thus widely established. But it results in a world of barriers and mistrust. Moreover: <em>closed source</em> is hindering concerning progress and individuality in all fields.</p>
		<br />
		<p>As a consequence I license all my works with a <a href="http://creativecommons.org/">creative commons</a> licence, allowing others to use and alter it as they wish, and encourage others to do so, too. I also use free and open source software wherever I can.</p>
	</div>

