<h2 id="merch_bd">Blu-ray Box</h2>
<p>Released on December 19, 2012 in Japan. Includes three Blu-ray discs with episodes 1-13 and the OVA (10.5), a CD with the soundtrack and a booklet with a small story guide, interviews and a very short, manga bonus chapter.</p>
