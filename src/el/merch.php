<h2 id="merch_dvd">DVDs</h2>
<p>There are several different versions:</p>
<br />
<p><strong id="merch_slim">Slimline Complete Collection</strong></p>
<p>Probably the cheapest and easiest way to get a physical copy of the whole anime. The Slimline Complete Collection is one "box" including four DVDs (Vector 1-4) and a card.</p><img src="src/el/merch/slim_00.jpg" /><img src="src/el/merch/slim_01.jpg" /><br /><br />
<p><strong id="merch_vectors">Single DVDs (Vectors)</strong></p>
<p>The same four discs as in the slimline edition, but each one in a single DVD box. They may include a reverse cover and a special like a sticker or poster.</p>
<img src="src/el/merch/vectors_00.jpg" />
<p>In detail:</p>
<p><strong>Vector One</strong> contains episode 1-4 and may include a sticker.</p><img src="src/el/merch/vectors_01.jpg" />
<p><strong>Vector Two</strong> contains episode 5-7 and may include a poster (with a print on both sides).</p><img src="src/el/merch/vectors_02.jpg" />
<p><strong>Vector Three</strong> contains episode 8-10 and may include a card (the same as in the slimline box).</p><img src="src/el/merch/vectors_03.jpg" />
<p><strong>Vector Four</strong> contains episode 11-13.</p><img src="src/el/merch/vectors_04.jpg" />
<p><strong>Reverse Covers</strong> may be covers of the Japanese DVDs (of which there are 7, called notes and not vectors) or more shocking images than the normal covers.</p><img src="src/el/merch/vectors_06.jpg" /><img src="src/el/merch/vectors_05.jpg" /><br ><br />
<p><strong id="merch_collbox">Collector's box</strong></p>
<p>The collector's box is used for the four single DVDs (Vector 1-4). Including the DVDs the box sells for about 160&euro; (~230&dollar;) on ebay if it's in mint or very good condition.</p><img src="src/el/merch/collbox_00.jpg" /><img src="src/el/merch/collbox_01.jpg" /><br /><br />
<p><strong id="merch_report">Diclonius Report</strong></p>
<p>An alternate collectors box where the series is distributed on three DVDs. The DVDs aren't called Vectors but Volumes.</p><img src="src/el/merch/report_00.jpg" /><img src="src/el/merch/report_01.jpg" /><img src="src/el/merch/report_02.jpg" /><br /><br />
<p><strong id="merch_notes">Single DVDs (Notes)</strong></p>
<p>In Japan Elfen Lied also was released on seven separate DVDs with 2 episodes per DVD. The DVDs aren't called Vectors or Volumes but Notes.</p>
<img src="src/el/merch/notes_00.jpg" />
<p>There's a limited edition of the 1st note which includes the Elfen Lied OST. This is the cover:</p><img src="src/el/merch/notes_01.jpg" /><br /><br />
